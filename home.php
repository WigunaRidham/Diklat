                                  <h2 align="center">SEJARAH PUSDIKLAT BAHASA BADIKLAT KEMHAN                                  </h2>
                                    <p align="justify" style="margin:30px;">Pusat Pendidikan dan Pelatihan   (Pusdiklat) Bahasa pada awal dibentuk bernama Prolemsa (Proyek Lembaga   Bahasa)Hankam yang didirikan pada tanggal 30 Desember 1976 dan sebagai   kepala proyek ditunjuklah Brigjen TNI H. Nastap S.A Tiga tahun kemudian, yakni pada tanggal   15 Oktober 1979, Prolemsa Hankam berubah menjadi Lemsa Hankam dibawah   komando langsung Kepala Staf Angkatan Laut. Dengan demikian maka tanggal   15 Oktober 1979 adalah pertama kalinya terbentuk sebuah organisasi   struktural yang mengadakan pengendalian program pendidikan bahasa untuk   personel Dephankam / ABRI. Atas dasar itu maka ditetapkan tanggal 15   Oktober 1979 sebagai Hari Jadi Pusbasa Hankam. Pada awal tahun 1985 Lemsa Hankam   berubah nama menjadi Sebasa Hankam yang berlokasi di Lembaga Kedokteran   Gigi Angkatan Laut, Jalan Bendungan Jatiluhur No. 130, Pejompongan,   Jakarta Pusat.  Pada tanggal 10 Juli 1991, Sebasa Hankam berubah nama   menjadi Pusbasa Hankam .Perubahan nomenklatur Pusdiklat Bahasa selanjutnya berdasarkan :                                    </p>
       
                                  <table width="100%">
								  <tr>
								  <td colspan="20%"> 
								  <td width="70%">
                                                  <p align="justify">a. Surat Keputusan Menhankam nomor    Kep/11/M/VI/1999 tertanggal 22 Juni 1999, Pusbasa Hankam berubah nama    menjadi Pusbasa Dephankam hingga tahun 2000.</p>
                                                  <p align="justify">b. Keputusan Menhan Nomor:   Kep/19/M/XII/2000 tanggal 29 Desember 2000, Dikarenakan reformasi   TNI/Polri yang  berimbas keluarnya unsur Polri dari jajaran Dephankam   dan ABRI yang ditandai dengan perubahan nama     Dephankam menjadi   Dephan pada tahun 2000 menjadikan Pusbasa  Dephankam berbuah nama   menjadi Pusdiklat    Bahasa Dephan.</p>
                                                  <p align="justify">c. Peraturan Menhan RI Nomor:   PER/01/M/VIII/2005 tanggal 25 Agustus 2005, Pusdiklat Bahasa Dephan   berubah nama menjadi Pusdiklat Bahasa Badiklat Dephan.</p>
                                                  <p align="justify">d. Peraturan Menhan RI Nomor: 16 Tahun   2010 tanggal 27 September 2010, Pusdiklat Bahasa Dephan berubah nama    menjadi Pusdiklat Bahasa Badiklat Kemhan.</p>
